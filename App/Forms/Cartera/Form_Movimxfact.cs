namespace App.Forms.Cartera
{
    public partial class Form_Movimxfact : Form
    {
        public Form_Movimxfact()
        {
            InitializeComponent();
        }

        private void check_desbala_CheckedChanged(object sender, EventArgs e)
        {
            if (check_desbala.Checked == true)
            {
                lbl_descuadre.Visible = true;
                rb_caja.Visible = true;
                rb_factura.Visible = true;
            }
            else
            {
                lbl_descuadre.Visible = false;
                rb_caja.Visible = false;
                rb_factura.Visible = false;
            }
        }


    }
